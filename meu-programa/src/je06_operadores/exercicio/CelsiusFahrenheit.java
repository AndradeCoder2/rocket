package je06_operadores.exercicio;

public class CelsiusFahrenheit {
    public static void main(String[] args) {
        // Converter graus Celsius para Fahrenheit: Peça a temperatura em Celsius e mostre o valor equivalente em Fahrenheit.

        int celsius = 2;

        double conversao = (celsius * 1.8) + 32;
        System.out.println( "Converssão de Celsius para Fahrenheit é: " + conversao);

    }
}
