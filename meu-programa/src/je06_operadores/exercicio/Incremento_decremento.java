package je06_operadores.exercicio;

public class Incremento_decremento {
    public static void main(String[] args) {
        int x = 5;

        //Operador de incremento (Pós)
        System.out.println(x++);

        //Operador incremento (Pré)
        System.out.println(++x);

        //Operador de decremento (Pré)
        System.out.println(--x);

        //Operador de decremento (pós)
        System.out.println(x--);
    }
}
